<h2>FLE-APIってなんだろうか</h2>

<p>そのツールはAPI（アプリケーションプログラミングインタフェース）である。フランス語を習ってる日本人のために作られた。</p>
This is an application programming interface (API). Its purpose is to provide japanese language related functions, such as conjugation automatisms, sentence generation and other goodies. Other websites, irc bots are most welcome to use it for free.</p>

